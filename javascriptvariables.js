//variable declarations
//var,let use to defined variables types
//let -> keyword to declare variable
//= -> assignment operator
//; ->end of the statement
let a=20;
let b=30;
let sum=a+b;
console.log('The sum of'+a+','+b+'is:'+sum);

//ES6 -> Template string / template literal (``) back tick
console.log(`The Sum of ${a},${b} is: ${sum}`);


